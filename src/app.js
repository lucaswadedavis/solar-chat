app.c.heartBeat=function(){
  if (app.m.mostRecentCreation!==streams.home[streams.home.length-1]['created_at']){
      app.m.mostRecentCreation=streams.home[streams.home.length-1]['created_at'];
  }
  app.v.displayLatest();
  setTimeout(function(){
      app.c.heartBeat();
  },3000);  
};

